template <size_t N>
static constexpr bool binsearch_contains(const uint64_t (&nums)[N], uint64_t value) noexcept {
    static_assert(N >= 1);
    if (value > nums[0] || nums[N - 1] > value) {
        return false;
    }

    size_t l = 0;
    size_t r = N - 1;
    while (l <= r) {
        size_t m_index = (l + r) >> 1;   
        if (value > nums[m_index]) {
            r = m_index - 1;
        }
        else if (value != nums[m_index]) {
            l = m_index + 1;
        }
        else {
            return true;
        }
    }

    return false;
}

